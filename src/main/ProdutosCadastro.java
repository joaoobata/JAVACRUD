    public void carregarCategorias() {
        categoriaDAO dao = new categoriaDAO();
        List<categoria> lista = dao.listarCategorias();
        comboCategoria.removeAllItems(); // Limpa antes de adicionar
        for (categoria c : lista) {
            comboCategoria.addItem(c);
        }
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            produtos p = new produtos();
            p.setNome(nomeProduto.getText());
            p.setPreco(Float.parseFloat(precoProduto.getText()));
            p.setCod_Barras(codbarrasProduto.getText());
            
            // ðŸ”¥ Pega o OBJETO selecionado no ComboBox
            categoria cat = (categoria) comboCategoria.getSelectedItem();
            p.setCategoria(cat.getId()); // <-- Aqui pega o ID da categoria
            produtoDAO dao = new produtoDAO();
            dao.inserir(p);
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
            
            // Limpa os campos depois de salvar
            nomeProduto.setText("");
            precoProduto.setText("");
            codbarrasProduto.setText("");
            comboCategoria.setSelectedIndex(0);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
                                    
